package net.ememed.user2.message_center.entity;

public class SmsSettingDetail {
	private String SMS_ORDER;
	private String SMS_APPLY_CASH;
	private String SMS_ACTIVITY;
	
	public String getSMS_ORDER() {
		return SMS_ORDER;
	}
	public void setSMS_ORDER(String sMS_ORDER) {
		SMS_ORDER = sMS_ORDER;
	}
	public String getSMS_APPLY_CASH() {
		return SMS_APPLY_CASH;
	}
	public void setSMS_APPLY_CASH(String sMS_APPLY_CASH) {
		SMS_APPLY_CASH = sMS_APPLY_CASH;
	}
	public String getSMS_ACTIVITY() {
		return SMS_ACTIVITY;
	}
	public void setSMS_ACTIVITY(String sMS_ACTIVITY) {
		SMS_ACTIVITY = sMS_ACTIVITY;
	}
}
